function myFunction(){
    //Find maximum no from given series of numbers
    var first_number=10;    //declaration of first number
    var second_number=30;   //declaration of second number
    var third_number=20;    //declaration of Third number
    var maxNo = Math.max(first_number, second_number, third_number); //ath.max() used to get maximum no.
    console.log("Maximum number from 10,30,20 is :"+maxNo);
}

myFunction();